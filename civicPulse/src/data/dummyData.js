// Calculate statistics
export const getStatistics = () => {
  const current = issuesData.filter(issue => issue.status === "current").length;
  const pending = issuesData.filter(issue => issue.status === "pending").length;
  const resolved = issuesData.filter(issue => issue.status === "resolved").length;
  const critical = issuesData.filter(issue => issue.status === "critical").length;

  return { current, pending, resolved, critical };
};

// Get issue distribution by type for pie chart
export const getIssueDistribution = () => {
  const distribution = {};
  issuesData.forEach(issue => {
    distribution[issue.type] = (distribution[issue.type] || 0) + 1;
  });

  return Object.entries(distribution).map(([name, value]) => ({
    name,
    value,
  }));
};
